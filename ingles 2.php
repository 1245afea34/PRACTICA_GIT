                    <h3> Indefinite articles a vs an</h3>
                    <p class="texto-antes">Today we are going to learn about the indefinite articles "a" and "an". <br>
(Hoy vamos a aprender sobre los artículos indefinidos "a" y "an").
<br>
In English, we use "a" or "an" to talk about one thing or person, but in a general way — not <br>
(En inglés, usamos "a" o "an" para hablar de una sola cosa o persona, pero de manera general — no específica).
<br>
<br>
(¿Cuándo usamos "a"?) <br>

We use "a" before words that start with a consonant <br>
(Usamos "a" antes de palabras que comienzan con un sonido de consonante).
<br>
📌 Examples:
<br>
a cat (un gato) <br>
a house (una casa) <br>
a dog (un perro) <br>
a book (un libro) <br>

</p>
                
                <p class="texto-despues">🔤 When do we use "an"? <br>
(¿Cuándo usamos "an"?)

We use "an" before words that start with a vowel sound (a, e, i, o, u). <br>
(Usamos "an" antes de palabras que comienzan con un sonido de vocal: a, e, i, o, u).

📌 Examples:
<br>
an apple (una manzana) <br>
an elephant (un elefante) <br>
an igloo (un iglú) <br>
an orange (una naranja) <br>
an umbrella (un paraguas) <br>
<br>
Important Tip:
It’s not just about the letter the word starts with, but the sound. <br>
(No solo se trata de la letra con la que comienza la palabra, sino del sonido).
<br>
Example:
a university ❌ "an university" — (porque "university" suena como "you-niversity", empieza con sonido de consonante). <br>
a unicorn (un unicornio) <br>
✔️ a university <br>    
✔️ a unicorn</p>
