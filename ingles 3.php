                    <h3> Simple Past </h3>
                    <p class="texto-antes">⏳ Simple Past – Pasado Simple <br>
🧠 ¿Para qué se usa? <br>
El Simple Past se usa para hablar de acciones que comenzaron y terminaron en el pasado.
<br>
📍 I visited my grandmother yesterday. <br>
(Visité a mi abuela ayer). <br>
<br>
🧱 Estructura: <br>
✅ Afirmativa: <br>
[Sujeto] + [verbo en pasado] + [complemento] <br>
<br>
I played soccer last weekend. (Jugué fútbol el fin de semana pasado). <br>
<br>
❌ Negativa: <br>
[Sujeto] + did not (didn't) + [verbo en infinitivo] <br>

She didn’t go to school. (Ella no fue a la escuela). <br>
<br>    
❓ Interrogativa: <br>
Did + [sujeto] + [verbo en infinitivo]? <br>

Did you watch the movie? (¿Viste la película?)</p>
