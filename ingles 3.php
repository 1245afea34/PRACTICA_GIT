                    <p class="texto-antes">
                        🧱 Estructura gramatical: <br>
✅ Afirmativa: <br>
[Sujeto] + was/were + verbo con -ing + complemento <br>

Sujeto	Verbo auxiliar	Ejemplo <br>
I / He / She / It	was	She was reading a book. (Ella estaba leyendo un libro.) <br>
You / We / They	were	They were playing soccer. (Ellos estaban jugando fútbol.) <br>
