                    <p class="texto-antes">🧱 Estructura del Past Continuous (Pasado Continuo): <br>
📝
Subject + was/were + verb(-ing) + complement <br>

(Sujeto + was/were + verbo con -ing + complemento) <br>
<br>
✅ Ejemplos afirmativos: <br>
Subject	was/were	Verb + ing	Complement	Español  <br>
I	was	studying	English	Yo estaba estudiando inglés. <br>
He / She / It	was	cooking	dinner	Ella estaba cocinando la cena. <br>
You / We / They	were	playing	soccer	Ellos estaban jugando fútbol. <br>
<br>
❌ Estructura negativa: <br>
Subject + was/were + not + verb(-ing) +  <br>

I was not watching TV. (Yo no estaba viendo la tele.) <br>

They were not doing homework. (Ellos no estaban haciendo la tarea.) <br>
<br>
❓ Estructura interrogativa: <br>
Was/Were + subject + verb(-ing) + complement? <br>

Was she reading a book? (¿Estaba ella leyendo un libro?) <br>

Were you sleeping at 10 p.m.? (¿Estabas durmiendo a las 10 p.m.?)</p>
