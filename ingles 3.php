❌ Negativa: <br>
[Sujeto] + was/were + not + verbo con -ing <br>
<br>
He was not (wasn’t) listening. (Él no estaba escuchando.) <br>
We were not (weren’t) studying. (No estábamos estudiando.) <br>
<br>
❓ Interrogativa: <br>
Was/Were + sujeto + verbo con -ing? <br>
<br>
Was she cooking dinner? (¿Ella estaba cocinando la cena?) <br>
Were you doing your homework? (¿Estabas haciendo tu tarea?) <br>
<br>
